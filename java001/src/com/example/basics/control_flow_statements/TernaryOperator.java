package com.example.basics.control_flow_statements;

public class TernaryOperator {
    public static void main(String[] args) {

        //podobne do if i else

        int a = 8;
        int b = (a>3) ? 100 : 10;
        System.out.println(b);

        int c = 10;
        int d = (c>100) ? 100 : 10;
        System.out.println(d);

    }
}
